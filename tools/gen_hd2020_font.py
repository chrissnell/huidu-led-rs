def pack_glyph(rows):
    assert len(rows) == CELL_H, rows
    out = bytearray()
    for row in rows:
        assert len(row) == CELL_W, row
        bits = 0
        for col, ch in enumerate(row):
            if ch == "#":
                bits |= 1 << (CELL_W - 1 - col)
        out.append(bits)
    return out


def main():
    here = os.path.dirname(os.path.abspath(__file__))
    out_path = os.path.join(here, "..", "crates", "huidu-proto", "src", "hd2020", "font5x7.bin")
    out_path = os.path.normpath(out_path)
    blob = bytearray()
    for cp in range(FIRST, LAST + 1):
        ch = chr(cp)
        if ch not in GLYPHS:
            raise SystemExit(f"missing glyph for {ch!r} (0x{cp:02x})")
        blob += pack_glyph(GLYPHS[ch])
    expected = (LAST - FIRST + 1) * CELL_H
    assert len(blob) == expected, (len(blob), expected)
    with open(out_path, "wb") as f:
        f.write(blob)
    print(f"wrote {out_path}: {len(blob)} bytes, {LAST - FIRST + 1} glyphs")


if __name__ == "__main__":
    main()
